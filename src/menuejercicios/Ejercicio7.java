    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double precio_base;
        double precio_instal; //El precio de la instalacion con el tipo de dato double
        double precio_formacion;
        double precio_alimentacion;
        
        //Recojo datos desde la ventana
        
        precio_base = Double.parseDouble(txtPrecioBase.getText());
        precio_instal = Double.parseDouble(etiPrecioInstalacion.getText());
        precio_formacion = Double.parseDouble(etiPrecioFormacion.getText());
        precio_alimentacion = Double.parseDouble(etiPrecioAlimentacionBD.getText());
        // Ahora que tengo los datos puedo hacer calculos
        
        // Al precio base se le van a√±adiendo precios extras
        // Segun esten o no activados los botones Togglebuton double precio_total
        
        double precio_total;
        precio_total = precio_base;
        
        if (tbtnInstalacion.isSelected()) {
            precio_total = precio_total+precio_instal;
        }
        if (tbtnFormacion.isSelected()) {
            precio_total = precio_total+precio_formacion;
        }
        if (tbtnAlimentacionBD.isSelected()) {
            precio_total = precio_total+precio_alimentacion;
        }
        // Finalmente pongo el resultado final en la etiqueta(Label)
        
        etiTotal.setText(precio_total+" Euros");
        
        // setText para enviar datos
        // getText para agarrar datos
        
    }//GEN-LAST:event_btnCalcularActionPerformed

