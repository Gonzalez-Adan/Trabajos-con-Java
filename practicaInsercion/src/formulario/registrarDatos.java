    private void txtNombreKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txtNombre.getText().trim().equals("")) {
               JOptionPane.showMessageDialog(null,"Este campo no debe quedar vacio", "Atencion",JOptionPane.ERROR_MESSAGE);
               txtNombre.setText("");
               txtNombre.requestFocus();
               return;
                
            } else {
                txtApellido.requestFocus();
                txtApellido.setEnabled(true);
            }
        }
    }//GEN-LAST:event_txtNombreKeyPressed

    private void guardarDatos() {
        // Obtener los datos del formulario
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String cedula = txtCedula.getText();
        
        
        // Conectar a la base de datos
        Connection conexionDB = con.conectar();
        
        // Definir la tabla y los campos
        String tabla = "alumno"; // Cambia esto al nombre real de tu tabla
        String campos = "alum_nombre, alum_apellido, alum_cedula";
        String valores = "'" + nombre + "', '" + apellido + "', '" + cedula + "'";
        
        // Insertar datos en la base de datos
        con.insertar_datos(tabla, campos, valores, 0); // Puedes ajustar el valor del mensaje como necesites
        
        // Limpiar los campos del formulario
        txtNombre.setText("");
        txtApellido.setText("");
        txtCedula.setText("");
        
        // Cerrar la conexi√≥n
        con.cerrarConexion();
    }
    

    
    
